package com.astonitas.view.frameworks.demo.bean;

public abstract class BaseBean {

	private Long id;
	private Long type;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getType() {
		return type;
	}

	public void setType(Long type) {
		this.type = type;
	}

}
